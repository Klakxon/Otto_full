package com.example.Otto.models.enums;

/**
 * The Specificity enum represents different specificity levels for perfumes.
 * Each constant in this enum corresponds to a specific specificity level.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum Specificity {
    /**
     * Represents perfumes suitable for everyday use.
     */
    Повсякденні,

    /**
     * Represents perfumes suitable for special occasions.
     */
    Спеціальні,

    /**
     * Represents perfumes suitable for work environments.
     */
    Робота,

    /**
     * Represents perfumes suitable for recreational activities.
     */
    Відпочинок
}

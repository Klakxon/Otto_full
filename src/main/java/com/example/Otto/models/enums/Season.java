package com.example.Otto.models.enums;

/**
 * The Season enum represents different seasons of the year.
 * Each constant in this enum corresponds to a specific season.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum Season {
    /**
     * Represents Summer.
     */
    Літо,

    /**
     * Represents Autumn.
     */
    Осінь,

    /**
     * Represents Winter.
     */
    Зима,

    /**
     * Represents Spring.
     */
    Весна
}

package com.example.Otto.models.enums;

/**
 * The Aroma enum represents various types of perfume aromas.
 * This enumeration is used to categorize perfumes based on their scent profiles.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum Aroma {
    /**
     * Floral aromas are typically derived from flowers and have a sweet, romantic, and fresh scent.
     */
    Квіткові,

    /**
     * Citrus aromas are derived from citrus fruits like lemon, orange, and bergamot, known for their fresh, tangy, and zesty scent.
     */
    Цитрусові,

    /**
     * Oriental aromas are characterized by warm, exotic, and spicy notes, often including ingredients like vanilla, musk, and spices.
     */
    Східні,

    /**
     * Woody aromas are earthy and warm, derived from woods like sandalwood, cedar, and vetiver.
     */
    Деревні,

    /**
     * Foguerie's aromas are typically a blend of lavender, oakmoss, and coumarin, offering a fresh, herbal, and slightly woody scent.
     */
    Фужерні
}

package com.example.Otto.models.enums;

/**
 * The Saturation enum represents different levels of saturation for a perfume.
 * Each level corresponds to the intensity or richness of the fragrance.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum Saturation {
    /**
     * LIGHT represents a fragrance with a subtle and delicate intensity.
     */
    Легкі,

    /**
     * RICH represents a fragrance with a strong and intense saturation.
     */
    Насичені,

    /**
     * MODERATE represents a fragrance with a balanced and medium level of saturation.
     */
    Помірні
}

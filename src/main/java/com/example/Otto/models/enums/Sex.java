package com.example.Otto.models.enums;

/**
 * The Sex enum represents different genders or sex categories.
 * Each constant in this enum corresponds to a specific sex category.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum Sex {
    /**
     * Represents the male gender.
     */
    Чоловічі,

    /**
     * Represents the female gender.
     */
    Жіночі,

    /**
     * Represents a gender-neutral category or suitable for both sexes.
     */
    Унісекс
}

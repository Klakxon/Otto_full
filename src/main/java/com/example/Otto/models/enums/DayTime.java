package com.example.Otto.models.enums;

/**
 * The DayTime enum represents different times of the day for which a perfume might be suitable.
 * This enumeration helps in categorizing perfumes based on the ideal time for their use.
 *
 * @author Dmytro Ukrainets
 * @version 1.0
 * @since 11.06.2024
 */
public enum DayTime {
    /**
     * Morning time is characterized by the early hours of the day, often associated with fresh and light scents.
     */
    Ранок,

    /**
     * Day time refers to the period during the daylight hours, suitable for vibrant and energetic scents.
     */
    День,

    /**
     * Evening time is associated with the later part of the day, often calling for deeper, more intense scents.
     */
    Вечір
}
